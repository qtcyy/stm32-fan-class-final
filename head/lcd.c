#include "lcd.h"
#include "fml_lcd.h"
#include "stdio.h"

/*********************************************************************************************
 * 名称:lcd_init()
 * 功能:LCD初始化并打印实验基本信息
 * 参数:name -- 实验名称
 * 返回:无
 *********************************************************************************************/
void lcd_init(unsigned char name) {
  LCD_DriverInit();
  LCD_Clear(0xFFFF);
  LCD_FillColor(0, 0, 319, 30, 0x4596);
  LCDDrawFnt24(80, 5, "风扇智能调节系统", 0xFFD700, 0x4596);
  LCD_FillColor(0, 240 - 30, 319, 240, 0x4596);
  LCDDrawFnt24(96, 240 - 30 + 5, "嵌入式实践设计", 0xFFD700, 0x4596);
}
